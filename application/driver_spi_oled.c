void oled_init(void)
{
#ifdef OLED_USE_GPIO
    // GPIO Initialized in driver_all_board.c
#endif
    
#ifdef OLED_USE_SPI
    // SPI Initialized in driver_all_board.c
#endif

    OLED_RST_SET();
	//delay_ms(100);
	oled_delay(0xffff);
	OLED_RST_CLR();
	//delay_ms(100);
	oled_delay(0xffff);
	OLED_RST_SET(); 
	oled_delay(0xffff);
    
	oled_byte_write(0xAE, OLED_CMD);//--turn off oled panel
	oled_byte_write(0x00,OLED_CMD);//---set low column address
	oled_byte_write(0x10,OLED_CMD);//---set high column address
	oled_byte_write(0x40,OLED_CMD);//--set start line address  Set Mapping RAM Display Start Line (0x00~0x3F)
	oled_byte_write(0x81,OLED_CMD);//--set contrast control register
	oled_byte_write(0xCF,OLED_CMD); // Set SEG Output Current Brightness
	oled_byte_write(0xA1,OLED_CMD);//--Set SEG/Column Mapping     0xa0???? 0xa1??
	oled_byte_write(0xC8,OLED_CMD);//Set COM/Row Scan Direction   0xc0???? 0xc8??
	oled_byte_write(0xA6,OLED_CMD);//--set normal display
	oled_byte_write(0xA8,OLED_CMD);//--set multiplex ratio(1 to 64)
	oled_byte_write(0x3f,OLED_CMD);//--1/64 duty
	oled_byte_write(0xD3,OLED_CMD);//-set display offset	Shift Mapping RAM Counter (0x00~0x3F)
	oled_byte_write(0x00,OLED_CMD);//-not offset
	oled_byte_write(0xd5,OLED_CMD);//--set display clock divide ratio/oscillator frequency
	oled_byte_write(0x80,OLED_CMD);//--set divide ratio, Set Clock as 100 Frames/Sec
	oled_byte_write(0xD9,OLED_CMD);//--set pre-charge period
	oled_byte_write(0xF1,OLED_CMD);//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	oled_byte_write(0xDA,OLED_CMD);//--set com pins hardware configuration
	oled_byte_write(0x12,OLED_CMD);
	oled_byte_write(0xDB,OLED_CMD);//--set vcomh
	oled_byte_write(0x40,OLED_CMD);//Set VCOM Deselect Level
	oled_byte_write(0x20,OLED_CMD);//-Set Page Addressing Mode (0x00/0x01/0x02)
	oled_byte_write(0x02,OLED_CMD);//
	oled_byte_write(0x8D,OLED_CMD);//--set Charge Pump enable/disable
	oled_byte_write(0x14,OLED_CMD);//--set(0x10) disable
	oled_byte_write(0xA4,OLED_CMD);// Disable Entire Display On (0xa4/0xa5)
	oled_byte_write(0xA6,OLED_CMD);// Disable Inverse Display On (0xa6/a7) 
	oled_byte_write(0xAF,OLED_CMD);//--turn on oled panel
	
//	oled_byte_write(0xAF,OLED_CMD); /*display ON*/ 
	oled_disp_clr();
	oled_set_pos(0,0); 	
}

