#ifdef OLED_USE_GPIO
void oled_byte_write(uint8_t data,uint8_t cmd)
{	
    uint8_t i;			  
    if(cmd)
    {
	  OLED_DC_SET();
    }
	else
    {
	  OLED_DC_CLR();
    }        
	OLED_CS_CLR();
	for(i=0; i<8; i++)
	{
		OLED_CK_CLR();
		if(data&0x80)
        {
		   OLED_MO_SET();
        }
		else
        {
		   OLED_MO_CLR();
        }
        oled_delay(20);
		OLED_CK_SET();
		data <<= 1;
        oled_delay(20);        
	}				 		  
	OLED_CS_SET();
	OLED_DC_SET();   	  
}
#endif

#ifdef OLED_USE_SPI
void oled_byte_write(uint8_t data,uint8_t cmd)
{
    if(cmd)
    {
	  OLED_DC_SET();
    }
	else
    {
	  OLED_DC_CLR();
    }        
	OLED_CS_CLR();
    while (!(Chip_SPI_GetStatus(OLED_SPI_PORT) & SPI_STAT_TXRDY)) {}; /* Make sure the last frame sent completely */
    Chip_SPI_SendMidFrame(OLED_SPI_PORT, data);
    while (!(Chip_SPI_GetStatus(OLED_SPI_PORT) & SPI_STAT_TXRDY)) {}; /* Make sure the last frame sent completely */
	OLED_CS_SET();
	OLED_DC_SET();   	  
}
#endif


void oled_set_pos(uint8_t x, uint8_t y) 
{ 
	oled_byte_write(0xb0+y, OLED_CMD);
	oled_byte_write(((x&0xf0)>>4)|0x10, OLED_CMD);
	oled_byte_write((x&0x0f)|0x01, OLED_CMD); 
}

void oled_disp_on(void)
{
	oled_byte_write(0X8D, OLED_CMD);  // SET DCDC
	oled_byte_write(0X14, OLED_CMD);  // DCDC ON
	oled_byte_write(0XAF, OLED_CMD);  // DISPLAY ON
}


void oled_display_off(void)
{
	oled_byte_write(0X8D, OLED_CMD);  // SET DCDC
	oled_byte_write(0X10, OLED_CMD);  // DCDC OFF
	oled_byte_write(0XAE, OLED_CMD);  // DISPLAY OFF
}	

void oled_disp_clr(void)  
{  
	uint8_t i,n;		    
	for(i=0;i<8;i++)  
	{  
		oled_byte_write(0xB0+i,OLED_CMD);
		oled_byte_write(0x00,OLED_CMD);
		oled_byte_write(0x10,OLED_CMD);
		for(n=0;n<128;n++)
        {
            oled_byte_write(0,OLED_DATA);
        }
	} 
}

void oled_disp_char(uint8_t x, uint8_t y, uint8_t chr)
{      	
    uint8_t c = 0, i = 0;	
    c = chr - ' ';		
    if( x > (OLED_MAX_COL-1) )
    {
        x = 0;
        y = y + 2;
    }
    if(OLED_SIZE ==16)
    {
        oled_set_pos(x,y);	
        for(i=0;i<8;i++)
        {
            oled_byte_write(FONT8X16[c*16+i],   OLED_DATA);
        }
        oled_set_pos(x,y+1);
        for(i=0;i<8;i++)
        {
            oled_byte_write(FONT8X16[c*16+i+8], OLED_DATA);
        }
    }
    else
    {	
        oled_set_pos(x,y+1);
        for(i=0;i<6;i++)
        {
            oled_byte_write(FONT6x8[c][i],      OLED_DATA);
        }
    }
}

uint32_t oled_disp_pow(uint8_t m, uint8_t n)
{
    uint32_t result=1;	 
    while(n--)
    {
        result *= m; 
    }        
    return result;
}

void oled_disp_num(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size)
{         	
	uint8_t t,temp;
	uint8_t enshow=0;						   
	for(t = 0; t < len; t++)
	{
		temp=(num/oled_disp_pow(10,len-t-1))%10;
		if(enshow==0&&t<(len-1))
		{
			if(temp==0)
			{
				oled_disp_char(x+(size/2)*t,y,' ');
				continue;
			}else enshow=1; 
		 	 
		}
	 	oled_disp_char(x+(size/2)*t,y,temp+'0'); 
	}
}

void oled_disp_string(uint8_t x,uint8_t y,uint8_t *chr)
{
	unsigned char j=0;
	while (chr[j]!='\0')
	{
        oled_disp_char(x,y,chr[j]);
        x+=8;
		if(x>120)
        {
            x=0;y+=2;
        }
        j++;
	}
}

